# < LESS THAN
# > GREATER THAN
# <= LESS THAN OR EUQAL TO
# >= GREATER THAN OR EQUAL TO 
# == EQUAL TO 
# != NOT EQUAL TO


year = 2022


print(year != 2022)
print(year < 2022)
print(year > 2022)
print(year <= 2022)
print(year >= 2022)


if year == 2022:
    print("HOOORAY")


year = 2021

