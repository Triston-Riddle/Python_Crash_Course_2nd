# if elif else block that deterines someones age

age = 21

if age < 2:
    print("you're a baby!")
elif age < 4:
    print("you're a toddler!")
elif age < 13:
    print("you're a kid")
elif age < 20:
    print("you're a teenager")
elif age < 65:
    print("you're an adult!")
elif age >= 65:
    print("you are an elder!")
else:
    print("you do not exist")
